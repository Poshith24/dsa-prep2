package com.posh.STR;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a= 367.1284f;
        System.out.printf("Formatted number is %.2f",a); // It also rounds off.
        System.out.println();
        System.out.printf("Pie: %.4f",Math.PI);
        System.out.println();
        String name = "Poshith";
        System.out.printf("I am %s and also %s",name,"Kumar");

    }
}
